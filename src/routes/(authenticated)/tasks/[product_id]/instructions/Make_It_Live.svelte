<h3>Instructions</h3>
<p>
  For apps being added to Scriptoria that are already published to Google Play, most of these steps
  have already been completed. You will still need to perform the last step to complete the rollout
  of the draft publish of the new version of the app.
</p>
<ul>
  <li>
    Go to <a href="https://play.google.com/apps/publish/" target="_blank">
      Google Play Developer Console
    </a>
    and sign in.
  </li>
  <li>
    Select <strong>Grow users</strong>
    &gt;
    <strong>Store presence</strong>
    section in the menu on the left.
  </li>
  <li>
    Select <strong>Store settings</strong>
    in the menu on the left and do the following:
  </li>
  <ul>
    <li>
      Click the <strong>Edit</strong>
      link next to the
      <strong>App category</strong>
      section:
    </li>
    <li>
      Set the <strong>Category</strong>
      to
      <em>Books & Reference</em>
      (or appropriate category)
    </li>
    <li>
      Click the <strong>Save</strong>
      button at the bottom of the popup and close the popup window.
    </li>
    <li>
      Click on <strong>Manage tags</strong>
      link and set appropriate tags (e.g. Religious text) and click the
      <strong>Apply</strong>
      button at the bottom of the page.
    </li>
    <li>
      Click the <strong>Edit</strong>
      link next to the
      <strong>Store listing contact details</strong>
      section:
    </li>
    <li>
      Set <strong>Email address</strong>
    </li>
    <li>
      Set <strong>Website</strong>
      (optional)
    </li>
    <li>
      Click the <strong>Save</strong>
      button at the bottom of the popup and close the popup window.
    </li>
  </ul>

  <li>
    To complete the rollout, Select <strong>Test and release</strong>
    &gt;
    <strong>Production</strong>
    in the menu on the left and do the following:
  </li>
  <ul>
    <li>
      Click on the <strong>Releases</strong>
      tab in the middle of the page.
    </li>
    <li>
      Click the <strong>Edit release</strong>
      link.
    </li>
    <li>
      Click the <strong>Next</strong>
      button at the bottom of the page.
    </li>
    <li>
      Click the <strong>Save</strong>
      button at the bottom of the page.
    </li>
    <li>
      Click the <strong>Go to Overview</strong>
      button on the
      <strong>Go to Publishing overview?</strong>
      dialog.
    </li>
    <li>
      Click the <strong>Send XX Changes for reivew</strong>
      button.
    </li>
    <li>
      Click the <strong>Send changes for review</strong>
      button on the
      <strong>Send XX changes for review?</strong>
      dialog.
    </li>
  </ul>
</ul>
